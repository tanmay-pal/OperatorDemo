class AssignmentOperator {
	public static void main(String[] args) {
		// Declaring variables
		int num;
		String name;

		// Assigning values
		num = 10;
		name = "Lumen";

		// Displaying the assigned values
		System.out.println("num is assigned: " + num);
		System.out.println("name is assigned: " + name);
	}
}
