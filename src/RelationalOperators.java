public class RelationalOperators {
	public static void main(String[] args) {

		int num1 = 1;
		int num2 = 2;

		System.out.println("num1 > num2 is " + (num1 > num2));
		System.out.println("num1 < num2 is " + (num1 < num2));
		System.out.println("num1 >= num2 is " + (num1 >= num2));
		System.out.println("num1 <= num2 is " + (num1 <= num2));
		System.out.println("num1 == num2 is " + (num1 == num2));
		System.out.println("num1 != num2 is " + (num1 != num2));
	}
}
